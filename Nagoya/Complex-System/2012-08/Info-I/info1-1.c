#include <stdio.h>

/**
 * 名古屋大学　大学院情報学研究科　2013年度博士前期課程入試問題　情1-1
 *
 * ---問題---
 * 次のプログラムの出力結果を示せ
 */
int main(void)
{
    int a = 0xf123;

    /**
     * Hex | Bin
     * 0xf123 | 1111 0001 0010 0011 
     * << 2: 0011 1100 0100 1000 1100 === 0x3c48c => 3c48c
     * >> 4: 0000 1111 0001 0010 === 0x0f12 => f12
     * 
    */
    printf("%x\n %x\n", a << 2, a >> 4);
    return 0;
}