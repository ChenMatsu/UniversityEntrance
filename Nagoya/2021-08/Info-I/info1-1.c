#include <stdio.h>

/**
 * 名古屋大学　大学院情報学研究科　2022年度博士前期課程入試問題　情1−１
 */
int main(void)
{
    /**
     * @Note
     *      start with "0x" means hexadecimal
     *      start with "0" means octal decimal
     *
     */
    int x1 = 5321, x2 = 0xf0f0, x3 = 037;
    /*
     *   Decimal | Hex | Octal
     *    5321 =>  14c9
     *   61680 <=  f0f0
     *     31  <=         037
     *
     */
    printf("%x\n", x1);
    printf("%d\n", x1 - x2); // 5321 - 61680 = 1680 + 54679 = -56359
    printf("%d\n", x2 + x3); // 61680 + 31 = 61711
    printf("%d\n", x2 + x3); // 61680 + 31 = 61711
    printf("%x\n", x1 >> 2); // 5321 / 2 => 2660 / 2 => 1330 => 532 or 0x0532 

    return 0;
}

// 5321 => Hex
// 16 * 16 = 256 * 4 = 1024
// 256 * 16 = 2 ^ 8 * 2 ^ 4 = 2 * 12 = 4096
// 5321 - 4096 = 321 + 904 => 1225 - 1024 = 25 + 176 = 201
// 16 * 10 = 160
// 16 * 12 = 192

// f0f0 => Dec
// 16^1 * 15 = 160 + 80 = 240
// 16^3 * 15 = (2^4)^3 * 15 = 2^64 * 15 = 61440
// 240 + 61440 = 61680

// 1330 => Hex
// 256 * 5 = 1024 + 256 = 1280 => 1330 - 1280 = 50
// 16 * 3 = 48 => 50 - 48 = 2
