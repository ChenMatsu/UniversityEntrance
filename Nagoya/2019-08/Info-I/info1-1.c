#include <stdio.h>

/**
 * 名古屋大学　大学院情報学研究科　2020年度博士前期課程入試問題　情1−１
 * 
 * ---問題---
 * 次のプログラムの出力結果を示せ
 */

int main()
{

    int a[] = {100, 0x200, 0300}, *p = a;

    printf("%x\n", a[0]);      // 100 => Hex => 0x64
    printf("%d\n", *(p + 1));  // 0x200 => Decimal => 16^2 * 2 = (2^4)^2 * 2 = 2^8 * 2 = 512
    printf("%o\n", a[1] << 1); // 0x200 => Decimal => 512 => 512 << 1 === 512 * 2 = 1024 => Oct => 2000 => 8^3 * 2 = 512 * 2
    // (*p + 1) => 取得當前a的位址後+1 === a[0] + 1  => 101 | 192  === (1100101 | 11000000) => (11100101) = 1*1 + 4*1 + 32*1 + 64*1 + 128*1 = 192 + 32  = 224 + 4 + 1 = 229
    printf("%d\n", (*p + 1) | a[2]);
    return 0;
}

